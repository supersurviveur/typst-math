#[derive(Debug, Clone)]
#[wasm_bindgen(getter_with_clone)]
pub struct Symbol {
    pub symbol: char,
    pub category: Category,
}

#[derive(Debug, Clone)]
#[wasm_bindgen]
pub enum Category {
    KEYWORD,
    OPERATOR,
    DEFAULT,
}
impl fmt::Display for Category {
    fn fmt(&self, f: &mut fmt::Formatter) -> fmt::Result {
        fmt::Debug::fmt(self, f)
    }
}

#[derive(Debug, Clone, Copy)]
#[wasm_bindgen]
pub enum Color {
    KEYWORD,
    NUMBER,
}

